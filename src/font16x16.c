uint8_t get_font_index(uint8_t c) {
  if (c >= 'A' && c <= 'Z') {
    return c - 'A';
  } else if (c >= '0' && c <= '9') {
    return DIGIT_INDEX + c - '0';
  } else if (c == '!') {
    return EXCLAM_INDEX;
  } else if (c == ',') {
    return COMMA_INDEX;
  } else if (c == '.') {
    return PERIOD_INDEX;
  }
  return EMPTY_INDEX;
}

